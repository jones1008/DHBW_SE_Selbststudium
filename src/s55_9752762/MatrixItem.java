package s55_9752762;

public class MatrixItem {
    private int index;
    private int value;

    public MatrixItem(int index, int value) {
        this.index = index;
        this.value = value;
    }

    public int getIndex() {
        return index;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }
}
